        destructive:
          "bg-gradient-to-r from-destructive to-destructive/80 text-white shadow-md shadow-destructive/25 hover:shadow-lg hover:shadow-destructive/30 hover:scale-[1.02] active:scale-[0.98] focus-visible:border-destructive/40 focus-visible:ring-destructive/20",
